public class Calculator {

    public float adding(int a, int b) {
        return a + b;
    }

    public float subtract(int a, int b) {
        return a - b;
    }

    public float multiply(int a, int b) {
        return a * b;
    }

    public float divide(int a, int b) {
        return a / b;
    }


}
